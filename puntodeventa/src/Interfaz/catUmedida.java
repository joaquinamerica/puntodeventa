    private void tablaumedidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaumedidaMouseClicked
        if (btnnuevo.getText().equals("Nuevo")) {
            int fila;
            fila = tablaumedida.rowAtPoint(evt.getPoint());
            if (fila > -1) {
                txtClave.setText(String.valueOf(tablaumedida.getValueAt(fila, 0)));
                txtdescripcion.setText(String.valueOf(tablaumedida.getValueAt(fila, 1)));
                btnEliminar.setEnabled(true);
                btnModificar.setEnabled(true);
            }
        }
    }//GEN-LAST:event_tablaumedidaMouseClicked

