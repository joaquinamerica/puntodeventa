    void llenar() {
        String[] titulos = {"Clave", "Descripcion"};
        mtabla = new DefaultTableModel(null, titulos);
        rs = UmeDao.catUmedida();
        String[] fila = new String[2];
        try {
            while (rs.next()) {
                fila[0] = rs.getString("ID");
                fila[1] = rs.getString("Descripcion");
                mtabla.addRow(fila);
            }
            tablaumedida.setModel(mtabla);
        } catch (Exception e) {
        }
    }

    public void maxid() {
        String maximo;
        maximo = UmeDao.maximoId();
        txtClave.setText(maximo);
    }

    void limpiar() {
        txtClave.setText("");
        txtdescripcion.setText("");
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }

    public void asignarEntradas() {
        txtClave.setText(txtClave.getText().trim());
        txtdescripcion.setText(txtdescripcion.getText().trim());
        UmeBean = new UmedidaBean();
        UmeBean.setIdumedida(Integer.parseInt(txtClave.getText()));
        UmeBean.setDescripcion(txtdescripcion.getText());
    }

    public void insertarumedida() {
        String rpta;
        asignarEntradas();
        UmeBean.setProceso(1);
        rpta = UmedidaNe.CUDUmedida(UmeBean);
        if (rpta == null) {
            JOptionPane.showMessageDialog(null, "Unidad de medida agregada con éxito");
        } else {
            JOptionPane.showMessageDialog(null, "Error en Ingreso " + rpta);
        }
    }
