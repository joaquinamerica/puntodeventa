    void llenar() {
        String[] titulos = {"Clave", "Nombre", "Telefono", "Direccion", "Localidad", "CURP", "RFC"};
        mtabla = new DefaultTableModel(null, titulos);
        rs = CteDao.catcliente();
        String[] fila = new String[7];
        try {
            while (rs.next()) {
                fila[0] = rs.getString("Clave");
                fila[1] = rs.getString("Nombre");
                fila[2] = rs.getString("Telefono");
                fila[3] = rs.getString("Direccion");
                fila[4] = rs.getString("Localidad");
                fila[5] = rs.getString("CURP");
                fila[6] = rs.getString("RFC");
                mtabla.addRow(fila);
            }
            tablacliente.setModel(mtabla);
        } catch (Exception e) {
        }
    }

