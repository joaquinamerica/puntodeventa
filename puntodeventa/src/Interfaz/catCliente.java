    private void tablaclienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaclienteMouseClicked
        if (btnnuevo.getText().equals("Nuevo")) {
            int fila;
            fila = tablacliente.rowAtPoint(evt.getPoint());
            if (fila > -1) {
                txtclave.setText(String.valueOf(tablacliente.getValueAt(fila, 0)));
                rs = CteDao.buscarcliente(Integer.parseInt(txtclave.getText()));
                try {
                    while (rs.next()) {
                        txtrazonsocial.setText(rs.getString("Nombre"));
                        txttelefono.setText(rs.getString("Telefono"));
                        txtdireccion.setText(rs.getString("Direccion"));
                        txtreferencia.setText(rs.getString("referencia"));
                        txtciudad.setText(rs.getString("localidad"));
                        txtestado.setText(rs.getString("estado"));
                        txtcp.setText(rs.getString("cp"));
                        txtcurp.setText(rs.getString("CURP"));
                        txtrfc.setText(rs.getString("RFC"));
                        txtobservacion.setText(rs.getString("observaciones"));
                    }
                } catch (Exception e) {
                }

                btneliminar.setEnabled(true);
                btnmodificar.setEnabled(true);

            }
        }
    }//GEN-LAST:event_tablaclienteMouseClicked

    private void btnnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevoActionPerformed
        if (btnnuevo.getText().equals("Nuevo")) {
            btneliminar.setEnabled(false);
           btnmodificar.setEnabled(false);
            btnnuevo.setText("Guardar");
            limpiar();
            maxid();
        } else {
            if (txtrazonsocial.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campos descripcion vacio, no se guardo en la base de datos");
            } else {
                insertarcliente();
            }
            limpiar();
           btneliminar.setEnabled(false);
          btnmodificar.setEnabled(false);
          btnnuevo.setText("Nuevo");
        }
        llenar();

    }//GEN-LAST:event_btnnuevoActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
         String rpta;
        asignarEntradas();
         CteBean.setProceso(2);
        rpta =ClienteNE.CUDCliente(CteBean);
        if (rpta == null) {
            JOptionPane.showMessageDialog(null, "Cliente modificado con éxito");
        } else {
            JOptionPane.showMessageDialog(null, "Error en Ingreso: " + rpta);
        }
        limpiar();
        llenar(); 
    }//GEN-LAST:event_btnmodificarActionPerformed

